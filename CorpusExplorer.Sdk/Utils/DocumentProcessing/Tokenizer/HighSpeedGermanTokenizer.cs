    public override string Execute(string input)
    {
      input = input.Length < _max ? SpecialTokenizerCleanup(input) : SpecialTokenizerCleanupForExtraLongInput(input);

      int length;
      do
      {
        length = input.Length;
        input = input.Replace("  ", " ");
      } while (length != input.Length);

      return input.Replace("< ENDOFCORPUSEXPLORERFILE >", "<ENDOFCORPUSEXPLORERFILE>").Trim().Replace(" ", Environment.NewLine);
    }

    private string SpecialTokenizerCleanup(string str)
    {
      var input = new StringBuilder(str);

      try
      {
        input = _conv1.Aggregate(input, (current, pair) => current.Replace(pair.Key, pair.Value));
      }
      catch (Exception ex)
      {
        InMemoryErrorConsole.Log(ex);
      }

      try
      {
        input = _conv2.Aggregate(input, (current, pair) => current.Replace(pair.Key, pair.Value));
      }
      catch (Exception ex)
      {
        InMemoryErrorConsole.Log(ex);
      }

      try
      {
        input = _conv3.Aggregate(input, (current, pair) => current.Replace(pair.Key, pair.Value));
      }
      catch (Exception ex)
      {
        InMemoryErrorConsole.Log(ex);
      }

      try
      {
        input = _conv4.Aggregate(input, (current, pair) => current.Replace(pair.Key, pair.Value));
      }
      catch (Exception ex)
      {
        InMemoryErrorConsole.Log(ex);
      }

      try
      {
        input = _conv5.Aggregate(input, (current, pair) => current.Replace(pair.Key, pair.Value));
      }
      catch (Exception ex)
      {
        InMemoryErrorConsole.Log(ex);
      }

      return input.ToString();
    }

    private string SpecialTokenizerCleanupForExtraLongInput(string input)
    {
      var cut = input.Length / _max;
      var slots = new string[input.Length % _max == 0 ? cut : cut + 1];
      Parallel.For(
                   0,
                   slots.Length,
                   Configuration.ParallelOptions,
                   i =>
                   {
                     var pos = i * _max;
                     slots[i] = SpecialTokenizerCleanup(
                                                        input.Substring(pos,
                                                                        pos + _max > input.Length
                                                                          ? input.Length - pos
                                                                          : _max));
                   });

      return string.Concat(slots);
    }
